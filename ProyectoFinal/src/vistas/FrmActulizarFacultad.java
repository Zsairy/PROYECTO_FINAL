    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        Connection cn;
        CallableStatement cs;
        try {
            Class.forName("org.postgresql.Driver");
            cn = DriverManager.getConnection("jdbc:postgresql://localhost:5432/Proyecto_final", "tutorias" ,"1234");
            cs=cn.prepareCall("select actividades.factualiza_facultad(?,?,?,?)");
            
            cs.setString(1 , txtNombre.getText());
            cs.setString(2 ,txtDescripcion.getText());
            cs.setInt(3 , Integer.parseInt(txtCodigosicoa.getText()));
            cs.setInt(4 , Integer.parseInt(txtCodigofacultad.getText()));
           
            if (cs.execute()) {
                JOptionPane.showMessageDialog(null, "Datos actulizados correctamente");
                
            }
        } catch (ClassNotFoundException | SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

